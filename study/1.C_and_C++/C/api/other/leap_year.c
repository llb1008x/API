int main()
{
    int year=0,day=0,ret=0,i=0,sum=0;
    int month[12]={31,28,31,30,31,30,31,31,30,31,30,31};
    
    printf("请输入年，月，日\n");
    scanf("%d %d %d",&year,&i,&day);

    ret=leap_year(year);
    if(ret==0){
        month[1]+1;    
        printf("是润年\n");
    }else{

        printf("不是润年\n");
    }
    
    for(ret=0;ret<i-1;ret++){
        
        sum += month[ret];
        printf("sum->%d\n",sum);
    }

    sum += day;
    printf("这是这一年的第%d天\n",sum);

    return 0;
}
