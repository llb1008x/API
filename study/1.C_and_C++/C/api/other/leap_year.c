#include <stdio.h>

/*********************************************************
*函数：判断是否是润年
*参数：输入的年份
*返回值：是或不是
* *******************************************************/
int leap_year(int year)
{
    if( ((year%4==0)&&(year%100!=0))||(year%400==0) ){

        return 0;
    }else {

        return -1;
    }
}

